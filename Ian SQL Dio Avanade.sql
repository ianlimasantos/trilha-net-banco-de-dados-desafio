SELECT F.Nome, G.Genero
FROM FILMES AS F INNER JOIN FILMESGENERO AS FG ON (F.Id = FG.IdFilme)
INNER JOIN Generos AS G ON (FG.IdGenero = G.Id)


-- QUESTÃO 11

SELECT F.Nome, G.Genero
FROM FILMES AS F INNER JOIN FILMESGENERO AS FG ON (F.Id = FG.IdFilme)
INNER JOIN Generos AS G ON (FG.IdGenero = G.Id)
WHERE G.Genero like '%Mistério%'

