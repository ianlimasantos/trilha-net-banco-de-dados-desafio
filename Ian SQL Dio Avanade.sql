-- QUESTÃO 6
SELECT NOME, ANO, DURACAO
FROM FILMES
WHERE DURACAO > 100 and DURACAO < 150
ORDER BY DURACAO


-- QUESTÃO 7

SELECT ANO, COUNT(ID) AS QUANTIDADE
FROM FILMES
GROUP BY ANO
ORDER BY COUNT(ID) DESC

